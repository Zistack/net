separator (char c)
{
	return c == '(' ||
		c == ')' ||
		c == '<' ||
		c == '>' ||
		c == '@' ||
		c == ',' ||
		c == ';' ||
		c == ':' ||
		c == '\\' ||
		c == '"' ||
		c == '/' ||
		c == '[' ||
		c == ']' ||
		c == '?' ||
		c == '=' ||
		c == '{' ||
		c == '}' ||
		c == ' ' ||
		c == '\t';
}
